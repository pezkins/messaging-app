// Search function
export function searchEmojis(query: string): EmojiData[] {
  if (!query.trim()) {
    return EMOJIS;
  }
  
  const lowerQuery = query.toLowerCase().trim();
  
  return EMOJIS.filter(emoji => 
    emoji.name.toLowerCase().includes(lowerQuery) ||
    emoji.keywords.some(keyword => keyword.toLowerCase().includes(lowerQuery)) ||
    emoji.category.toLowerCase().includes(lowerQuery)
  );
}

// Get emojis by category
export function getEmojisByCategory(category: EmojiCategory): EmojiData[] {
  if (category === 'Frequently Used') {
    return FREQUENT_EMOJIS.map(emoji => 
      EMOJIS.find(e => e.emoji === emoji) || { emoji, name: '', keywords: [], category: 'Frequently Used' }
    );
  }
  return EMOJIS.filter(emoji => emoji.category === category);
}

